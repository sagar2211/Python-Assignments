# What is different between List and Tuple in Python 

# Mutability 
# List : It is mutable. (It can be modified)
# Tuple : It is immutable. (It can't be modified)

# Memory
# List : Lists generally consume more memory than tuples in Python
# Tuple : Tuples generally consume less memory than lists in Python

# Performance
# List: Slower than tuple
# Tuple: Faster than list

# Use cases
# List : When data should be change then list will be used
# Tuple : When data should not be changed then tuple will be used